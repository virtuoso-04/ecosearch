// HACKATHON MOD: Multer middleware configuration for image uploads
// - Configured for product image uploads with size/type validation
// - Stores files in server/uploads with unique naming
// - Fallback handling for demo environment

const multer = require('multer');
const path = require('path');
const fs = require('fs');

// Ensure uploads directory exists
const uploadsDir = path.join(__dirname, '..', 'uploads');
if (!fs.existsSync(uploadsDir)) {
  fs.mkdirSync(uploadsDir, { recursive: true });
}

// Configure storage
const storage = multer.diskStorage({
  destination: (req, file, cb) => {
    cb(null, uploadsDir);
  },
  filename: (req, file, cb) => {
    // Create unique filename: timestamp-random-originalname
    const uniqueSuffix = Date.now() + '-' + Math.round(Math.random() * 1E9);
    const ext = path.extname(file.originalname);
    const name = path.basename(file.originalname, ext);
    cb(null, `${name}-${uniqueSuffix}${ext}`);
  }
});

// File filter for images only
const fileFilter = (req, file, cb) => {
  // Check if file is an image
  if (file.mimetype.startsWith('image/')) {
    cb(null, true);
  } else {
    cb(new Error('Only image files are allowed'), false);
  }
};

// Configure multer
const upload = multer({
  storage: storage,
  limits: {
    fileSize: 5 * 1024 * 1024, // 5MB limit
    files: 5 // Maximum 5 files
  },
  fileFilter: fileFilter
});

// HACKATHON MOD: Export different configurations for different use cases
module.exports = {
  // For product images (multiple files)
  uploadProductImages: upload.array('images', 5),
  
  // For single avatar uploads
  uploadAvatar: upload.single('avatar'),
  
  // For any files (generic)
  uploadAny: upload.any(),
  
  // Raw multer instance
  upload: upload
};
